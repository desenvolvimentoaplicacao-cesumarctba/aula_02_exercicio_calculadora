<?php

function somar($valor1,$valor2){
 $somar = $valor1 + $valor2;
 return $somar;
}
function multiplicar($valor1,$valor2){
 $multiplicar = $valor1 * $valor2;
 return $multiplicar;
}
function dividir($valor1,$valor2){
 $dividir = $valor1 / $valor2;
 return $dividir;
}
function subtrair($valor1,$valor2){
 $subtrair = $valor1 - $valor2;
 return $subtrair;
}